#pragma once

#include <pthread.h>

namespace restd {

class thread
{
  protected:

    pthread_t  _tid;
    int        _running;
    int        _detached;

  public:

    thread();
    virtual ~thread();

    int start();
    int join();
    int detach();
    pthread_t self();

    virtual void* run() = 0;
};

}
